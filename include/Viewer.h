#ifndef VIEWER_H
#define VIEWER_H

#include "FrameDrawer.h"
#include "MapDrawer.h"
#include "Tracking.h"
#include "System.h"

#include <mutex>

namespace ORB_SLAM3
{

class Tracking;
class FrameDrawer;
class MapDrawer;
class System;

class Viewer
{
public:
    Viewer(System* pSystem, FrameDrawer* pFrameDrawer, MapDrawer* pMapDrawer, Tracking *pTracking, const string &strSettingPath);

    // Main thread function. Draw points, keyframes, the current camera pose and the last processed
    // frame. Drawing is refreshed according to the camera fps. We use Pangolin.
    void Run();

    void RequestFinish();

    void RequestStop();

    bool isFinished();

    bool isStopped();

    bool isStepByStep();

    void Release();

    void SetTrackingPause();

    bool both;

    void Init();
    void Draw();

private:

    bool Stop();

    System* mpSystem;
    FrameDrawer* mpFrameDrawer;
    MapDrawer* mpMapDrawer;
    Tracking* mpTracker;

    // 1/fps in ms
    double mT;
    float mImageWidth, mImageHeight;

    float mViewpointX, mViewpointY, mViewpointZ, mViewpointF;

    bool CheckFinish();
    void SetFinish();
    bool mbFinishRequested;
    bool mbFinished;
    std::mutex mMutexFinish;

    bool mbStopped;
    bool mbStopRequested;
    std::mutex mMutexStop;

    bool mbStopTrack;

    bool menuFollowCamera;
    bool menuCamView;
    bool menuTopView;
    // bool menuSideView("menu.Side View",false,false);
    bool menuShowPoints;
    bool menuShowKeyFrames;
    bool menuShowGraph;
    bool menuShowInertialGraph;
    bool menuLocalizationMode;
    bool menuReset;
    bool menuStepByStep;  // false, true
    bool menuStep;

    pangolin::OpenGlRenderState s_cam;

    // Add named OpenGL viewport to window and provide 3D Handler
    pangolin::View d_cam;

    pangolin::OpenGlMatrix Twc, Twr;
    pangolin::OpenGlMatrix Ow; // Oriented with g in the z axis
    pangolin::OpenGlMatrix Twwp; // Oriented with g in the z axis, but y and x from camera

    bool bFollow;
    bool bLocalizationMode;
    bool bStepByStep;
    bool bCameraView;
};

}


#endif // VIEWER_H
	

